class Person:
    def __init__(self,fname,lname,email,phone,address):
        self.fname = fname
        self.lname = lname
        self.email = email
        self.phone = phone
        self.address = address

    def __init___():
        pass

    def toString(self):
        return {
            "First_name":self.fname,
            "Last_name":self.lname,
            "Email":self.email,
            "Phone":self.phone,
            "Address":self.address
        }